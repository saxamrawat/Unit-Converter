class Kiloto():
    def hecto(n):
        return n*10

    def deca(n):
        return n*100

    def meter(n):
        return n*1000

    def deci(n):
        return n*10000

    def centi(n):
        return n*100000

    def mili(n):
        return n*1000000

    def micro(n):
        return n*1000000000

    def nano(n):
        return n*1000000000000


