class Celsiusto():
    def fahrenheit(n):
        return (n*1.8)+32

    def kelvin(n):
        return n+273


class Fahrenheitto():
    def celsius(n):
        return (n-32)*0.55555555555

    def kelvin(n):
        return (n+459.67)*0.55555555555


class Kelvinto():
    def celsius(n):
        return n-273

    def fahrenheit(n):
        return ((n*1.8)-459.67)
